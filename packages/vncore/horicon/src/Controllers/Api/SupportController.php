    public function index(Request $request)
    {
        /** @var User $user */
        $user = Auth::user();

        $urgent = $request->get('urgent', 1);
        $perPage = $request->get('number', 20);
        $supports = SosSupport::where('user_id', $user->id)
            ->where('urgent', $urgent)
            ->orderBy('created_at', 'desc')->paginate($perPage);

        return new SosSupportCollection($supports);
    }

    public function store(Request $request)
    {
        /** @var User $user */
        $user = Auth::user();

        $data = $request->validate([
            'phone' => 'nullable',
            'content' => 'nullable',
            'location' => 'nullable',
            'lat' => ['required', 'regex:/([0-9.-]+).+?([0-9.-]+)/'],
            'lng' => ['required', 'regex:/([0-9.-]+).+?([0-9.-]+)/'],
            'urgent' => 'sometimes',
        ]);

        $urgent = $data['urgent'] ?? 1;
        $urgent = boolval($urgent);
        $data['urgent'] = $urgent;

        $support = new SosSupport();
        $support->fill($data);
        $support->user_id = $user->id;
        $support->status = 0;
        $support->save();

        $name = $user->name;
        $social = $user->email;
        $phone = $data['phone'] ?? $user->sosUser->phone;
        $content = $support->content;
        $subject = str_limit("COLAB SOS: $name - $social - $phone - $content", 78);

        $notifications = User::whereHas('roles', function ($query) {
            $query->where('name', 'notification');
        })->get();
        Mail::to($notifications)->sendNow(new SupportCreated($subject, $support, $user));

        if ($urgent) {
            $sms = $this->convert_vi_to_en("$name - $social - $phone - $content");
            $sms = $this->bienthuy_to_latin($sms);

            $sms = str_limit($sms, 160);
            $smsService = new SmsService();
            foreach ($notifications as $notification) {
                $smsService->sendSms($notification->phone, $sms);
            }
        }

//        $fcmService = new FcmService();
//        $fcmService->sendToTopic($support->title ? $support->title : 'Người dùng phản hồi hỗ trợ #' . $support->id, $support->content, $support, FcmService::TOPIC_SOS);

        return Response::json(['status' => 0]);
    }

    public function close(Request $request)
    {
        $id = $request->get('support_id');
        $support = SosSupport::findOrFail($id);
        $support->status = 1;
        $support->save();

        return Response::json(['status' => 0]);
